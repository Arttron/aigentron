#!/usr/bin/env node
// Generates prisma/sqlite/schema.prisma from prisma/schema.prisma (the single
// source of truth, used as-is by the `full`/postgres profile). Mechanical,
// lossless-except-for-two-things transform for the `minimal` profile
// (docs/plan-single-container.md, Phase 2):
//   1. datasource provider postgresql -> sqlite; generator output -> a
//      separate client dir (prisma-sqlite) so the two clients never collide.
//   2. Prisma has no enum support on SQLite: every `enum X { ... }` block is
//      dropped and fields typed `X` become `String`, with `@default(member)`
//      rewritten to `@default("member")`. Prisma also has no scalar-list
//      support on SQLite: the one such field (AgentEvent.attachments
//      String[]) becomes a JSON-encoded String.
// Run via `pnpm prisma:sync` after ANY schema.prisma edit, then commit the
// result — it is checked in (not gitignored) so schema drift is visible in
// review, not silent build magic.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const srcPath = join(here, 'schema.prisma');
const outPath = join(here, 'sqlite', 'schema.prisma');

const src = readFileSync(srcPath, 'utf8');
const lines = src.split('\n');

// Pass 1: collect enum names and the line ranges of `enum X { ... }` blocks.
const enumNames = new Set();
const enumLineRanges = [];
for (let i = 0; i < lines.length; i++) {
  const m = /^enum\s+(\w+)\s*\{/.exec(lines[i]);
  if (!m) continue;
  enumNames.add(m[1]);
  let j = i;
  while (!/^\}/.test(lines[j])) j++;
  enumLineRanges.push([i, j]);
}

// Pass 2: drop enum blocks (+ the blank line that follows each), rewrite
// enum-typed fields to String, and the one scalar-list field to a JSON string.
const drop = new Set();
for (const [start, end] of enumLineRanges) {
  for (let i = start; i <= end; i++) drop.add(i);
  if (lines[end + 1] === '') drop.add(end + 1);
}

const fieldRe = /^(\s*)(\w+)(\s+)(\w+)(\??)(\s*)(.*)$/;
const out = [];
for (let i = 0; i < lines.length; i++) {
  if (drop.has(i)) continue;
  const line = lines[i];

  const m = fieldRe.exec(line);
  if (m) {
    const [, indent, name, gap1, type, opt, gap2, rest] = m;
    if (enumNames.has(type)) {
      const rewrittenRest = rest.replace(/@default\((\w+)\)/, '@default("$1")');
      out.push(`${indent}${name}${gap1}String${opt}${gap2}${rewrittenRest}`.replace(/\s+$/, ''));
      continue;
    }
    if (type === 'String' && line.includes('[]') && /@default\(\[\]\)/.test(rest)) {
      // The one scalar list column (AgentEvent.attachments) -> JSON string.
      const rewrittenRest = rest.replace('[]', '').replace(/@default\(\[\]\)/, '@default("[]")');
      out.push(`${indent}${name}${gap1}String${opt}${gap2}${rewrittenRest}`.replace(/\s+$/, ''));
      continue;
    }
  }

  out.push(
    line
      .replace('output       = "../src/generated/prisma"', 'output       = "../../src/generated/prisma-sqlite"')
      .replace('provider = "postgresql"', 'provider = "sqlite"'),
  );
}

const header =
  '// GENERATED by prisma/generate-sqlite-schema.mjs from ../schema.prisma — do not hand-edit.\n' +
  '// Re-run `pnpm --filter @lds/orchestrator prisma:sync` after changing schema.prisma, then commit\n' +
  '// the diff (checked in on purpose, so drift between the two datasources is visible in review).\n' +
  '// Enums -> String and the one scalar-list column -> JSON string: SQLite supports neither natively\n' +
  '// (see docs/plan-single-container.md Phase 2). The @lds/shared union types remain the real contract.\n';

writeFileSync(outPath, header + out.join('\n'));
console.log(`Generated ${outPath}`);
